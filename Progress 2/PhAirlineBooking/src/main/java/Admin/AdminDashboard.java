    private void setupToggleButton() {
        toggleButton = new JButton("â˜°");
        toggleButton.setBackground(new Color(51, 51, 51)); 
        toggleButton.setForeground(Color.WHITE);
        toggleButton.setFocusPainted(false);
        toggleButton.setBorder(BorderFactory.createEmptyBorder(5, 10, 5, 10));
        
        JPanel buttonPanel = new JPanel(new FlowLayout(FlowLayout.LEFT));
        buttonPanel.setBackground(Color.WHITE);
        buttonPanel.add(toggleButton);
        
        Dashboard.add(buttonPanel, BorderLayout.NORTH);
        
        toggleButton.addActionListener(e -> toggleSidebar());
    }
    
    private void toggleSidebar() {
        if (animationTimer != null && animationTimer.isRunning()) {
            return; 
        }
        
      
        final int steps = 20; 
        final int delay = 10; 
        final int targetWidth = isSidebarVisible ? 0 : SIDEBAR_WIDTH;
        final int startWidth = isSidebarVisible ? SIDEBAR_WIDTH : 0;
        
        animationTimer = new Timer(delay, new ActionListener() {
            int step = 0;
            
            @Override
            public void actionPerformed(ActionEvent e) {
                step++;
               
                float progress = (float) step / steps;
                int newWidth = (int) (startWidth + (targetWidth - startWidth) * progress);
                
               
                SideBarPanel.setBounds(0, 0, newWidth, 600);
                Dashboard.setBounds(newWidth, 0, 1000 - newWidth, 600);
                
                getContentPane().revalidate();
                
              
                if (step >= steps) {
                    ((Timer)e.getSource()).stop();
                    isSidebarVisible = !isSidebarVisible;
                    SideBarPanel.setVisible(isSidebarVisible);
                }
            }
        });
        
        SideBarPanel.setVisible(true);
        animationTimer.start();
    }
    
    private void updateContentPanel(String menuItem) {
        Dashboard.removeAll();
        
        
        JPanel buttonPanel = new JPanel(new FlowLayout(FlowLayout.LEFT));
        buttonPanel.setBackground(Color.WHITE);
        buttonPanel.add(toggleButton);
        
        JLabel contentLabel = new JLabel(menuItem + " Page", SwingConstants.CENTER);
        contentLabel.setFont(new Font("Roboto", Font.BOLD, 28));
        
        Dashboard.add(buttonPanel, BorderLayout.NORTH);
        Dashboard.add(contentLabel, BorderLayout.CENTER);
        
        Dashboard.revalidate();
        Dashboard.repaint();
    }

    
  
    
